package week1.day2;

public class WhileLoop {

	// While loop is used to execute the program when we don't know the number of
	// times it should get executed
	// used when we know the condition

	public static void main(String[] args) {

		int x = 5;

		while (x < 10) {
			System.out.println(x);
			x = x + 1;
		}
	}

}
